package lesson_02;

public class BitWiseOperatorNumber {
    public static void main(String[] args) {
        // mach to hop (+), true => always true, false + false = false
        System.out.println(4 | 5);
            // 100 + 101 =101 => 5
        // mach chap (*, false => always false, true true = 1
        System.out.println(4 & 5);
            // 100 * 101 = 100 => 4

    }
}
